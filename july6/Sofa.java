class Sofa{

	String type;
	String shape;
	String color;
	double price;
	boolean quality;
	int seats;
	String brand;
	String style;

	Sofa(String type, String shape, String color, double price, boolean quality, int seats,String brand,String style){
		System.out.println("Sofa running with its properties");
		this.type=type;
		this.shape=shape;
		this.color=color;
		this.price=price;
		this.quality=quality;
		this.seats=seats;
		this.brand=brand;
		this.style=style;
	}
}